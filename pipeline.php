<script>
(function () {
    'use strict';

    const form          = document.getElementById('brochureForm');
    const nameEl         = document.getElementById('brName');
    const companyEl       = document.getElementById('brCompany');
    const emailEl         = document.getElementById('brEmail');
    const mobileEl        = document.getElementById('brMobile');
    const submitBtn      = document.getElementById('brSubmitBtn');
    const successPanel   = document.getElementById('brSuccessPanel');
    const doneBtn        = document.getElementById('brDoneBtn');
    const serverError    = document.getElementById('brServerError');
    const serverErrMsg   = document.getElementById('brServerErrorMsg');
    const modalEl        = document.getElementById('brochureModal');

    nameEl.addEventListener('blur',    () => validateField(nameEl,    'brNameErr',    /^.{2,}$/,                     'Please enter your full name (min. 2 characters).'));
    companyEl.addEventListener('blur', () => validateField(companyEl, 'brCompanyErr', /^.{2,}$/,                     'Please enter your company name.'));
    emailEl.addEventListener('blur',   () => validateField(emailEl,   'brEmailErr',   /^[^\s@]+@[^\s@]+\.[^\s@]+$/, 'Please enter a valid corporate email address.'));
    mobileEl.addEventListener('blur',  () => validateField(mobileEl,  'brMobileErr',  /^\+?[\d\s\-().]{7,20}$/,     'Please enter a valid mobile number.'));

    form.addEventListener('submit', function (e) {
        e.preventDefault();
        serverError.classList.remove('ct-alert--show');

        const valid = [
            validateField(nameEl,    'brNameErr',    /^.{2,}$/,                     'Please enter your full name (min. 2 characters).'),
            validateField(companyEl, 'brCompanyErr', /^.{2,}$/,                     'Please enter your company name.'),
            validateField(emailEl,   'brEmailErr',   /^[^\s@]+@[^\s@]+\.[^\s@]+$/, 'Please enter a valid corporate email address.'),
            validateField(mobileEl,  'brMobileErr',  /^\+?[\d\s\-().]{7,20}$/,     'Please enter a valid mobile number.')
        ].every(Boolean);

        if (!valid) return;

        submitBtn.classList.add('ct-submit-btn--loading');
        submitBtn.disabled = true;

        const formData = new FormData(form);

        fetch('send-brochure-request.php', { method: 'POST', body: formData })
            .then(function (res) { return res.json(); })
            .then(function (data) {
                submitBtn.classList.remove('ct-submit-btn--loading');
                submitBtn.disabled = false;

                if (data.success) {
                    form.reset();
                    form.style.display = 'none';
                    successPanel.classList.add('ct-success-panel--show');
                } else {
                    serverErrMsg.textContent = data.error || 'Something went wrong. Please try again.';
                    serverError.classList.add('ct-alert--show');
                }
            })
            .catch(function () {
                submitBtn.classList.remove('ct-submit-btn--loading');
                submitBtn.disabled = false;
                serverErrMsg.textContent = 'Network error. Please check your connection and try again.';
                serverError.classList.add('ct-alert--show');
            });
    });

    doneBtn.addEventListener('click', function () {
        const modal = bootstrap.Modal.getInstance(modalEl);
        if (modal) modal.hide();
    });

    /* Reset modal to form view each time it's opened/closed */
    modalEl.addEventListener('hidden.bs.modal', function () {
        successPanel.classList.remove('ct-success-panel--show');
        form.style.display = '';
        form.reset();
        form.querySelectorAll('.ct-input--valid, .ct-input--error').forEach(function (el) {
            el.classList.remove('ct-input--valid', 'ct-input--error');
        });
        serverError.classList.remove('ct-alert--show');
    });

    function validateField(el, errId, regex, msg) {
        const err = document.getElementById(errId);
        if (!regex.test(el.value.trim())) { showError(el, err, msg); return false; }
        clearError(el, err); return true;
    }

    function showError(el, errEl, msg) {
        el.classList.add('ct-input--error');
        errEl.textContent = msg;
        errEl.classList.add('ct-error--show');
    }

    function clearError(el, errEl) {
        el.classList.remove('ct-input--error');
        el.classList.add('ct-input--valid');
        errEl.classList.remove('ct-error--show');
    }
})();
</script>

